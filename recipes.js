// 레시피 클릭 시 정보를 보여주는 함수
function showRecipesList(recipeName) {
    const recipeBox = document.getElementById("recipe-box");
    const recipeList = document.getElementById("recipe-list");
    const methodList = document.getElementById("recipe-method");

    // 기존 내용 초기화
    recipeList.textContent = '';
    methodList.textContent = '';

    // 재료 리스트 생성
    recipeInfo[recipeName].ingredients.forEach(item => {
        const li = document.createElement('li');
        li.textContent = item;
        recipeList.appendChild(li);
    });

    // 만드는 방법 추가
    recipeInfo[recipeName].method.forEach(step => {
        const li = document.createElement('li');
        li.textContent = step;
        methodList.appendChild(li);
    });

    // 레시피 박스 표시
    recipeBox.style.display = 'block';
}

// 상자 닫기 버튼 이벤트 리스너
document.getElementById("close").addEventListener("click", function() {
    document.getElementById("recipe-box").style.display = 'none';
});